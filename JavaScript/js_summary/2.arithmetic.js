// Arithmetic operaters (산술 연산자)
// +
// -
// *
// -
// *

console.log(5 + 2);
console.log(5 - 2);
console.log(5 * 2);
console.log(5 / 2);
console.log(5 % 2);
console.log(Math.pow(5, 2));
console.log(5 ** 2); // es7

// + 연산자 주의점
let text = "Hello" + "World";
console.log(text);
text = "1" + 2; // 숫자와 문자열을 더하면 문자열로 반환됨
console.log(text);
